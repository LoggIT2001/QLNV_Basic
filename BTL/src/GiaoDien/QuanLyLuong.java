    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if(txtBacLuong.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Cần nhập bậc lương!");
            return;
        }
        model.setRowCount(0);
        try{
            LuongDao ld = new LuongDao();
            Luong li = ld.timKiemBacLuong(Integer.parseInt(txtBacLuong.getText()));
            if(li != null){
                model.addRow(li.toObjects());
            }else{
                JOptionPane.showMessageDialog(this, "Không tìm thấy lương!");
            }         
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed


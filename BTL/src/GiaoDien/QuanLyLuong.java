    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        LuongDao ld = new LuongDao();
        Luong pb = new Luong();
        String bl = JOptionPane.showInputDialog(null, "Nhập bậc lương cần xoá");
        pb.setBacLuong(Integer.parseInt(bl));

        int remove = tblLuong.getSelectedRow();
        int confirm = JOptionPane.showConfirmDialog(null, "Bạn có muốn xoá lương có bậc lương " + bl);

        try {
            if (confirm == JOptionPane.YES_OPTION) {
                if (ld.xoaLuong(Integer.parseInt(bl))) {
                    JOptionPane.showMessageDialog(null, "Xoá thành công!");
                    model.removeRow(remove);
                } else {
                    JOptionPane.showMessageDialog(null, "Xoá thất bại!");
                }
            } else {

            }
        } catch (Exception ex) {

        }
    }//GEN-LAST:event_btnDeleteMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        LuongDao ld = new LuongDao();
        Luong l = new Luong();
        l.setBacLuong(Integer.parseInt(txtBacLuong.getText()));
        l.setLuongCoBan(Integer.parseInt(txtLuongcb.getText()));
        l.setThuong(Integer.parseInt(txtThuong.getText()));

        if (txtBacLuong.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Hãy nhập bậc lương !");
            txtBacLuong.requestFocus();
        } else if (txtLuongcb.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Hãy nhập lương cơ bản !");
            txtLuongcb.requestFocus();
        } else if (txtThuong.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Hãy nhập thưởng  !");
            txtThuong.requestFocus();
        } else {
            if (ld.updateLuong(l)) {
                JOptionPane.showMessageDialog(null, "Sửa Thành Công!");
                int i = tblLuong.getSelectedRow();
                if (i >= 0) {
                    model.setValueAt(txtLuongcb.getText(), i, 1);
                    model.setValueAt(txtThuong.getText(), i, 2);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Sửa thất bại");
            }
            resetThongTin();
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

    private void btnNewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNewMouseClicked
        LuongDao ld = new LuongDao();
        String bacLuong = txtBacLuong.getText();
        String luongCoBan = txtLuongcb.getText();
        String thuong = txtThuong.getText();
        try {
            if (bacLuong.equals("") || luongCoBan.equals("") || thuong.equals("")) {
                JOptionPane.showMessageDialog(null, "Không được để trống !");
                return;
            }
            Luong luong = new Luong(Integer.parseInt(bacLuong), Integer.parseInt(luongCoBan), Integer.parseInt(thuong));
            for (Luong bl : LList) {
                if (luong.trungBacLuong(bl)) {
                    JOptionPane.showMessageDialog(null, "Mã đã tồn tại . Vui lòng nhập mã khác !");
                    return;
                }
            }
            if (ld.themLuong(luong)) {
                JOptionPane.showMessageDialog(null, "Thêm thành công !");
                model.addRow(luong.toObjects());
            } else {
                JOptionPane.showMessageDialog(null, "Mã đã tồn tại ! ");
            }
        } catch (Exception e) {
        }
        resetThongTin();
    }//GEN-LAST:event_btnNewMouseClicked

